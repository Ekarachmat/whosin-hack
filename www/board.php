	        function findNamePosition(name) {
	        	var foundRow = -1;

	        	for (var row=0; row < status.length; row++) {
	        		if (status[row].name.toUpperCase() === name.toUpperCase()) {
	        			foundRow = row;
	        			break;
	        		}
	        	}

	        	return foundRow;
	        }

	    	function flipToNameFunction(id, nameFrom, nameTo) {
	    		var index = 0;
	    		var clear = (nameFrom == "" && nameTo == "" ? true : false);

	    		var startPos = (nameFrom == "" ? 0 : findNamePosition(nameFrom));
	    		var endPos = (nameTo == "" ? status.length : findNamePosition(nameTo));

		        if (startPos == endPos) {
					var charName = sym.$(id);
					charName.text(status[startPos].name.toUpperCase());
		        } else {
			        var intObject = setInterval(function() {
						var charName = sym.$(id);

						charName.text(status[startPos].name.toUpperCase());

						startPos++;

						if(startPos == endPos){
							if (clear == false) {
								charName.text(status[startPos].name.toUpperCase());
							} else {
								charName.text("");
							}
						    clearInterval(intObject);
						}
					}, 50);
			    }
	    	}

			function flipToLetterFunction(id, letterFrom, letterTo){
		        var index = 0;

		        var startPos = letters.indexOf(letterFrom);
		        var endPos = letters.indexOf(letterTo);

		        if (startPos == endPos) {
					var charName = sym.$(id);
					charName.text(letters[startPos]);
